					bannerSlider.next()
				});
			});

			//头部浮动效果
			var head=$(".head").height();
			$(window).scroll(function(){
				var topScr=$(window).scrollTop();
				if (topScr>head) {
					$(".top").addClass("fixed");
				}else{
					$(".top").removeClass("fixed");
				}
			});

